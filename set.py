# operation in python set: union(|), intersection(&), difference(-), symmetric(^)

# to define the operations, define the sets

set1 = {0, 2, 4, 6, 8}
set2 ={1, 2, 3, 4, 5}

# union of sets

print(" UNION of sets:", set1 | set2)

print(" Intersection of sets:", set1 & set2)

print(" difference of sets:", set1 - set2)

print(" Symmetric of sets:", set1 ^ set2)